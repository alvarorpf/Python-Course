# Manejo de modulos en Python 3
# Dentro de un modulo se pueden realizar la declaracion tanto de funcioens, clases y atributos los cuales seran llamados desde otro programa
def sumar(a, b):
    return a + b

def restar(a, b):
    return a - b

def multiplicar(a, b):
    return a * b

def dividir(a, b):
    return a / b