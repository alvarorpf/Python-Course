# Variables de Tipo Booleano en Python

# Este tipo de de variable solo tiene dos valores especificos True o False
x = True
print(x)

y = False
print(y)

# Al utilizar un operador de comparacion "<" este almacena los resultados en la variable
num1 = 3
num2 = 2
resultado = num1 < num2
print(resultado)

# Tambien podemos utilizar una sentencia de control como el if
if(num1 < num2):
    print("El valor num1 es menor que num2")
else:
    print("El valor num1 no es menor que num2")    