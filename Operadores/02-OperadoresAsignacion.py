# Uso de Operadores de Asignacion

# Operador "=" este nos sirve para asignar valores a nuestras variables
x = 3
y = 5
print("X:", x,"Y:", y)

# Operador de Asignacion de Suma "+=" es lo mismo que x = x + 2
x += 2
print("Suma: ", x)

# Operador de Asignacion de Resta "-=" es lo mismo que x = x - 2
x -= 2
print("Resta: ", x)

# Operador de Asignacion de Multiplicacion "*=" es lo mismo que x = x * 2
y *= 2
print("Multiplicacion:", y)

# Operador de Asignacion de Division "/=" es lo mismo que x = x / 2
y /= 2
print("Division: ", y)