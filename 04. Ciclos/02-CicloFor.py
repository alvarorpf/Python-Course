# Uso de ciclo for en python
# Este ciclo es mas utilizado para revisar cada uno de los elementos de una coleccion o conjunto de datos

# Iteracion de una cadena
# El ciclo for se encarga de sacar de uno en uno los elementos (letras) que estan dentro de una coleccion de datos
# El comando else se puede utilizar para mostrar la finalizacion del ciclo for 
cadena = "Hola Mundo"
for letra in cadena:
    print(letra)
else:
    print("Fin del ciclo for")