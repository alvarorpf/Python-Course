# Uso de Operadores Aritmeticos en Python

# Suma
a = 3
b = 2
suma = a + b
print("Suma:", suma)

# Resta
resta = a - b
print("Resta:", resta)

# Multiplicacion
multi = a * b
print("Multiplicacion:", multi)

# Division
division = a / b
print("Division:", division)

# Modulo
modulo = a % b
print("Modulo:", modulo)

# Potencia
potencia = a ** b
print("Potencia:", potencia)